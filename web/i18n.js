  // Shared language contract with the Go backend: auto | en | zh-CN | zh-TW |
  // ja | ko | es | fr | de. Unknown codes fall back to English. Dictionaries
  // may be sparse — missing ones use the English table while still storing
  // the resolved BCP-47 code for document.lang / paint-cache.
  const SUPPORTED = new Set(['en', 'zh-CN', 'zh-TW', 'ja', 'ko', 'es', 'fr', 'de']);

  function normalizeLanguageCode(code) {
    const raw = String(code || '').trim().toLowerCase().replace(/_/g, '-');
    if (!raw || raw === 'auto') return '';
    if (raw === 'en' || raw.startsWith('en-')) return 'en';
    if (raw === 'zh' || raw === 'zh-cn' || raw === 'zh-hans' || raw.startsWith('zh-hans')) return 'zh-CN';
    if (raw === 'zh-tw' || raw === 'zh-hant' || raw.startsWith('zh-hant')) return 'zh-TW';
    if (raw === 'ja' || raw === 'ja-jp' || raw.startsWith('ja-')) return 'ja';
    if (raw === 'ko' || raw === 'ko-kr' || raw.startsWith('ko-')) return 'ko';
    if (raw === 'es' || raw.startsWith('es-')) return 'es';
    if (raw === 'fr' || raw.startsWith('fr-')) return 'fr';
    if (raw === 'de' || raw.startsWith('de-')) return 'de';
    // Bare region forms already handled; reject anything else (e.g. zh-hk).
    if (SUPPORTED.has(code)) return code;
    return '';
  }

  // The UI language is a server-owned setting (see setAppLanguage in app.js),
  // not something read from the phone/browser. `tvremote.lang` is only a
  // paint-cache of the last language the server resolved, so the very first
  // frame doesn't flash in the wrong language while /api/settings loads.
  const stored = normalizeLanguageCode(localStorage.getItem('tvremote.lang') || '');
  let lang = stored || EN;

  function format(template, params) {
    return String(template).replace(/\{(\w+)\}/g, (_, key) => {
      const value = params && params[key];
      return value == null ? '' : String(value);
    });
  }

  function t(key, params) {
    const dict = messages[lang] || messages[EN];
    const fallback = messages[EN][key] || messages[ZH][key] || key;
    return format(dict[key] || fallback, params);
  }

  function setLang(nextLang) {
    lang = normalizeLanguageCode(nextLang) || EN;
    localStorage.setItem('tvremote.lang', lang);
    document.documentElement.lang = lang;
    translate(document.body);
  }

  function translateTextNode(node) {
    if (node.parentElement?.hasAttribute('data-i18n')) return;
    const raw = node.nodeValue;
    const trimmed = raw.trim();
    if (!trimmed) return;
    const key = literalKeys[trimmed];
    if (!key) return;
    node.nodeValue = raw.replace(trimmed, t(key));
  }

  function translateElement(el) {
    if (!el || el.nodeType !== Node.ELEMENT_NODE) return;
    const textKey = el.getAttribute('data-i18n');
    if (textKey) {
      const next = t(textKey);
      if (el.textContent !== next) el.textContent = next;
    }
    const placeholderKey = el.getAttribute('data-i18n-placeholder');
    if (placeholderKey) {
      const next = t(placeholderKey);
      if (el.getAttribute('placeholder') !== next) el.setAttribute('placeholder', next);
    }
    for (const attr of ['aria-label', 'title', 'placeholder', 'data-placeholder']) {
      const value = el.getAttribute(attr);
      if (!value) continue;
      const key = literalKeys[value];
      if (!key) continue;
      // Only write when the value actually changes. Writing the identical value back
      // still fires the attributes MutationObserver below (subscribed on this same
      // attribute), which would call back into this function forever.
      const next = t(key);
      if (next !== value) el.setAttribute(attr, next);
    }
  }

  function translate(root) {
    if (!root) return;
    const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT | NodeFilter.SHOW_ELEMENT);
    let node = root.nodeType === Node.TEXT_NODE ? root : walker.currentNode;
    while (node) {
      if (node.nodeType === Node.TEXT_NODE) translateTextNode(node);
      else translateElement(node);
      node = walker.nextNode();
    }
  }

