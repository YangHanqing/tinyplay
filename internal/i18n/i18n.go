var preferred struct {
	sync.RWMutex
	value string
}

// SetPreferred selects a supported UI language or auto for native desktop UI.
func SetPreferred(value string) {
	value = strings.TrimSpace(value)
	if !isSupportedLanguage(value) {
		value = "auto"
	}
	preferred.Lock()
	preferred.value = value
	preferred.Unlock()
}

const (
	ZH = "zh-CN"
	EN = "en"
)

func isSupportedLanguage(value string) bool {
	switch value {
	case EN, ZH, "zh-TW", "ja", "ko", "es", "fr", "de":
		return true
	default:
		return false
	}
}

