func Normalize(value string) string {
	v := strings.ToLower(strings.ReplaceAll(value, "_", "-"))
	switch {
	case strings.HasPrefix(v, "zh-hant"), strings.HasPrefix(v, "zh-tw"), strings.HasPrefix(v, "zh-hk"):
		return "zh-TW"
	case strings.HasPrefix(v, "zh"):
		return ZH
	case strings.HasPrefix(v, "ja"):
		return "ja"
	case strings.HasPrefix(v, "ko"):
		return "ko"
	case strings.HasPrefix(v, "es"):
		return "es"
	case strings.HasPrefix(v, "fr"):
		return "fr"
	case strings.HasPrefix(v, "de"):
		return "de"
	default:
		return EN
	}
}

func SystemLang() string {
	preferred.RLock()
	override := preferred.value
	preferred.RUnlock()
	if isSupportedLanguage(override) {
		return override
	}
	for _, key := range []string{"LC_ALL", "LC_MESSAGES", "LANG"} {
		if value := os.Getenv(key); value != "" {
			return Normalize(value)
		}
	}
	if value := systemLocale(); value != "" {
		return Normalize(value)
	}
	return EN
}

func RequestLang(r *http.Request) string {
	if r == nil {
		return SystemLang()
	}
	if query := r.URL.Query().Get("lang"); query != "" {
		if normalized := Normalize(query); isSupportedLanguage(normalized) {
			return normalized
		}
	}
	header := r.Header.Get("Accept-Language")
	if header == "" {
		return SystemLang()
	}
	for _, part := range strings.Split(header, ",") {
		if normalized := Normalize(strings.TrimSpace(strings.SplitN(part, ";", 2)[0])); isSupportedLanguage(normalized) {
			return normalized
		}
	}
	return EN
}

func T(lang, key string, args ...any) string {
	lang = Normalize(lang)
	template := messages[lang][key]
	if template == "" {
		template = messages[EN][key]
	}
	if template == "" {
		template = key
	}
	if len(args) > 0 {
		return fmt.Sprintf(template, args...)
	}
	return template
}

func System(key string, args ...any) string {
	return T(SystemLang(), key, args...)
}

func Request(r *http.Request, key string, args ...any) string {
	return T(RequestLang(r), key, args...)
}

